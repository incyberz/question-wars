<h4>Buatlah Skema Database MySQL di PHPMyAdmin</h4>

<p>Skema database adalah sketsa atau diagram yang merujuk pada penataan atau organisasi data yang disimpan dalam basis data. Ada dua jenis skema, yaitu logis dan fisik. Skema logis mewakili bagaimana data ditata (disebut juga dengan ERD), sementara skema fisik menentukan struktur data penyimpanannya (Relasi Tabel). Yang akan kita pelajari adalah tentang Relasi Tabel.</p>

<p>Note: <code>Challenge Wajib "minimal satu relasi tabel"</code></p>


