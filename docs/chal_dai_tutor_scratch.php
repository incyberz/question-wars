<h3>Kunci sukses seorang pekerja salah satunya terletak dalam skill komunikasi!</h3>
<p>Jika kamu sudah belajar tentang Scratch maka saatnya kini kamu membagikan ilmu-mu secara online. Buatlah video tutorial Scratch bagi pemula sebagus mungkin.</p>
<h5>Langkah Persiapan:</h5>
<ul>
  <li>Siapkan kamera (hp, laptop, dll)</li>
  <li>Siapkan aplikasi perekam layar (Camtasia, Bandycam, OBS Studio, dll)</li>
  <li class="opsional">(opsional) Buat script rekaman!</li>
  <li class="opsional">(opsional) minta tolong kawanmu sebagai kameramen</li>
  <li class="opsional">(opsional) atur pencahayaan, lakukan di pagi hari</li>
  <li class="opsional">(opsional) test sound, pakai Clip-On microphone agar suara besar dan jernih</li>
</ul>

<h5>Langkah Pengerjaan</h5>
<ul>
    <li>Rekam layarmu untuk konten tutorial</li>
    <li>(opsional) Buatlah <code>video intro</code> perkenalan diri, terlihat wajah, sebutkan NIM, Nama, kelas. Intro example: Halo kawan2! saya Ahmad Firdaus, NIM 41414141, kelas TI-2025-REG-SORE akan menampilkan bukti challenge saya tentang [...]. Berikut videonya!</li>
  <li>(opsional) Buatlah <code>video outro</code>! Outro example: itulah video rekamannnya sebagai bukti bahwa saya telah praktikum mandiri dirumah. Terimakasih kepada [dosen, kawan] yang telah [...]. Sampai jumpa!</li>
  <li>(opsional) Gabungkan video tersebut (<code>join-video</code>) dan upload ke Youtube!</li>

  <li class="opsional">(opsional) Tambahkan keterangan video, images, smiley, meme, dll!</li>
  <li class="opsional">(opsional) Tambahkan video effect!</li>
  <li class="opsional">(opsional) Tambahkan sound effect!</li>
  <li>Submit Challenge!</li>
</ul>

