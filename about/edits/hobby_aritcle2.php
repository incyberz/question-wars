<p>Ayat tersebut dapat dikaitkan dengan bakat yang dimiliki setiap manusia. Dalam kata 
	<em>"setiap orang berbuat sesuai dengan pembawaannya masing-masing".
	</em>bahwa manusia terlahir dengan kemampuan atau bakat yang berbeda-beda.
</p>
<p>Sedangkan minat merupakan sesuatu yang berkembang karena suatu proses. Ada 4 hal yang dapat mempengaruhi minat seseorang diantaranya:
</p>
<ul>
	<li>Perhatian
	</li>
	<li>Rasa ingin tahu
	</li>
	<li>Kesempatan
	</li>
	<li>Kesenangan dan kenikmatan
	</li>
</ul>
<p>
</p>
<p>Dengan minat, seseorang akan termotivasi untuk mempelajari sesuatu yang ia minati. 
	<a target="_self" class="inner-link-tag" href="https://www.kompasiana.com/tag/minat" style="color:#428bca;">Minat
	</a> seseorang terhadap sesuatu dapat mempengaruhi bakat yang dimiliki orang tersebut. Ketika seseorang memiliki minat yang berkesinambungan dengan bakat yang ia miliki maka bakat orang tersebut akan berkembang dengan baik. Sebaliknya, bakat seseorang akan sulit berkembang jika orang tersebut tidak memiliki minat pada bidang yang berkaitan dengan bakatnya.
</p>
<p>semoga bermanfaat...
