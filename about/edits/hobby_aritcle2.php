
<p>
	<a target="_self" class="inner-link-tag" href="https://www.kompasiana.com/tag/bakat" style="color:#428bca;">Bakat
	</a> dan 
	<a target="_self" class="inner-link-tag" href="https://www.kompasiana.com/tag/minat" style="color:#428bca;">minat
	</a> sering sekali dianggap sama. Kedua kata ini sering tertukar posisinya dalam anggapan masyarakat. Lalu apakah yang membedakan dua hal tersebut?.
</p>
<p>Bakat dan minat merupakan dua hal yang berbeda sama sekali, namun keduanya saling berkaitan.&nbsp;
</p>
<p>Bakat merupakan suatu kemampuan yang dimiliki seseorang sejak ia lahir atau bisa dikatakan murni pemberian dari Allah SWT, dan tidak akan hilang. Hanya saja kadang seseorang tidak menyalurkan 
	<a target="_self" class="inner-link-tag" href="https://www.kompasiana.com/tag/bakat" style="color:#428bca;">bakat
	</a> yang dimiliki sehingga muncullah istilah bakat yang terpendam. Setiap individu lahir dengan membawa bakat mereka masing-masing. Dan antara individu yang satu dengan individu lain memiliki bakat berbeda.&nbsp;
</p>
